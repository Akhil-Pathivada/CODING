class Switch
{
	public static void main(String args[])
	{
		int number=20;
		switch(number)
		{
			case 10:
				System.out.println("\n 20 is greater than 10");
				break;
			case 20:
				System.out.println("\n 20 is equal to 20");
				break;
			default:
				System.out.println("\n nothing happened");
		}
	}
}